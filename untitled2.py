# Словник із користувачами та їх віковими групами
user_age_groups = {
    "Анна": "18-25",
    "Валентин": "26-35",
    "Віталій": "36-45",
    "Іван": "46-55",
    "Рома": "56+"
}

# Запитуємо ім'я користувача
name = input("Введіть ім'я користувача: ")

# Перевіряємо, чи є ім'я у словнику
if name in user_age_groups:
    print(f"Користувач {name} належить до вікової групи: {user_age_groups[name]}")
else:
    print(f"Користувач із ім'ям {name} не знайдений у словнику.")
